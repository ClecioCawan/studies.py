from math import radians, sin, cos , tan
a = float(input('Digite o 창gulo desejado:'))
sin = sin(radians(a))
cos = cos(radians(a))
tan = tan(radians(a))
print(f'O 창ngulo {a}, o seu SENO corresponde a {sin:.2f}')
print(f'O 창ngulo {a}, o seu COSSENO corresponde a {cos:.2f}')
print(f'O 창ngulo {a}, a sua TANGENTE corresponde a {tan:.2f}')